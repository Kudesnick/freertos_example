void vTaskA( void *pvParametrs)
{
	while(true)
    {
		vTaskDelay(1000);
	}
}

void vTaskB( void *pvParametrs)
{
	while(true)
    {
		vTaskDelay(1500);
	}
}

/***************************************************************************************************
 *                                        PUBLIC FUNCTIONS
 **************************************************************************************************/

int main(void)
{
    for (uint8_t i = 0; i < 30; i++)
    {
        xTaskCreate( &vTaskA,
                    (const char *)NULL,
                    configMINIMAL_STACK_SIZE, 
                    NULL,
                    1,
                    NULL );
    }
	xTaskCreate( &vTaskB, 
	             (const char *)NULL,
	             configMINIMAL_STACK_SIZE,
	             NULL,
	             1,
	             NULL );

    vTaskStartScheduler();
    
    return -1;
};

